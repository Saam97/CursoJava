private void salvarJogo() {
        // Pega os atribtos do jogo
        String nomeJogo = txt_nomeJogo.getText();
        String generoJogo = txt_generoJogo.getText();
        // Criando um novo objeto do tipo Jogo
        Jogo jogo = new Jogo();
        
        // Atribuindo os seus valores
        jogo.nome = nomeJogo;
        jogo.genero = generoJogo;
        
        // Adicionando no ArrayList
        games.add(jogo);
        // Salvando o ArrayList no nosso Arquivo .txt
        arquivo.jogoSalvar(games);
        // Exibindo uma Janelinha bonitinha
        JOptionPane.showMessageDialog(rootPane, "Jogo Salvo!");
        // Limpa os campos de textos
        limparCampos();
    }

    private void limparCampos() {
        // Seta os campos com vazio
        txt_generoJogo.setText("");
        txt_nomeJogo.setText("");
    }

    private void exibirJogos() {
        // "Retira" o ArrayList do arquivo .txt
        ArrayList<Jogo> leitura = arquivo.jogoLer();
        // Criando um array de Strings para armazenar o nome dos jogos
        String[] meusJogos = new String[leitura.size()];
        
        // Varrendo o array de Strings
        for (int i = 0; i < meusJogos.length; i++) {
            // Salvando cada nome de jogo em uma posiÃ§Ã£o do array de Strings
            meusJogos[i] = leitura.get(i).nome;
        }
        
        // Exibindo os jogos na lista
        lista_jogos.setListData(meusJogos);
    }
